        public ResultSet searchBarangByKeyword(String keyword) throws SQLException {
        String query = "SELECT kode_barang, nama_barang, merk, harga FROM tb_databarang WHERE nama_barang LIKE ?";
        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, "%" + keyword + "%");
        return pstmt.executeQuery();
        }

        public void updateStok(String kodeBarang, int jumlahBeli) throws SQLException {
            String query = "UPDATE tb_databarang SET stok = stok - ? WHERE kode_barang = ?";
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setInt(1, jumlahBeli);
            pstmt.setString(2, kodeBarang);
            pstmt.executeUpdate();
        }
        
        
