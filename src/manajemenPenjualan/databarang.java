    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if (txtKodeBarang.getText().isEmpty() || jTanggalMasuk.getDate() == null || jTanggalKeluar.getDate() == null ||
            txtNamaBarang.getText().isEmpty() || txtMerk.getText().isEmpty() || txtHarga.getText().isEmpty() || txtStok.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi data terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
       }
        
        try {
            String sql = "INSERT INTO tb_databarang (kode_barang, tgl_masuk, tgl_keluar, nama_barang, merk, harga, stok) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, txtKodeBarang.getText());
            pst.setDate(2, new java.sql.Date(jTanggalMasuk.getDate().getTime()));
            pst.setDate(3, new java.sql.Date(jTanggalKeluar.getDate().getTime()));
            pst.setString(4, txtNamaBarang.getText());
            pst.setString(5, txtMerk.getText());
            pst.setInt(6, Integer.parseInt(txtHarga.getText()));
            pst.setInt(7, Integer.parseInt(txtStok.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
            loadData();
            clearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        
        if (txtKodeBarang.getText().isEmpty() || jTanggalMasuk.getDate() == null || jTanggalKeluar.getDate() == null ||
            txtNamaBarang.getText().isEmpty() || txtMerk.getText().isEmpty() || txtHarga.getText().isEmpty() || txtStok.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
       }

       try {
           String sql = "UPDATE tb_databarang SET tgl_masuk=?, tgl_keluar=?, nama_barang=?, merk=?, harga=?, stok=? WHERE kode_barang=?";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setDate(1, new java.sql.Date(jTanggalMasuk.getDate().getTime()));
           pst.setDate(2, new java.sql.Date(jTanggalKeluar.getDate().getTime()));
           pst.setString(3, txtNamaBarang.getText());
           pst.setString(4, txtMerk.getText());
           pst.setInt(5, Integer.parseInt(txtHarga.getText()));
           pst.setInt(6, Integer.parseInt(txtStok.getText()));
           pst.setString(7, txtKodeBarang.getText());
           pst.executeUpdate();
           JOptionPane.showMessageDialog(null, "Data berhasil diubah");
           loadData();
           clearForm();
       } catch (SQLException e) {
           JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_btnUbahActionPerformed

